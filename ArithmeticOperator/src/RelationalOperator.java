public class RelationalOperator {
    public static void main(String[] args) {

        // we can take any number for a and b
        int a=22;
        int b=21;
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a>=b);

    }
}
